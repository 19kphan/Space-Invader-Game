uint16_t ADC0_InSeq3(void){  
	uint16_t result;
  ADC0_PSSI_R = 0x0008;            // 1) initiate SS3:1000
  while((ADC0_RIS_R&0x08)==0){};   // 2) wait for conversion done
  result = ADC0_SSFIFO3_R&0xFFF;   // 3) read result
  ADC0_ISC_R = 0x0008;             // 4) acknowledge completion
  return result;
}

void Delay100ms(unsigned long countEndTime){
  unsigned long volatile time;
  while(countEndTime>0){
    time = 72724;  // 0.1sec at 16 MHz 
    while(time){
	  	time--;
    }
    countEndTime--;
  }
}

uint8_t doOverlap(int topleft1x, int topleft1y, int bottomright1x, int bottomright1y,int topleft2x, int topleft2y, int bottomright2x, int bottomright2y)
{
    // if rectangle has area 0, no overlap
    if (topleft1x == bottomright1x || topleft1y == bottomright1y || bottomright2x == topleft2x || topleft2y == bottomright2y)
        return 0;
   
    // If one rectangle is on left side of other
    if (topleft1x > bottomright2x || topleft2x > bottomright1x)
        return 0;
 
    // If one rectangle is above other
    if (bottomright1y < topleft2y || bottomright2y < topleft1y)
        return 0;
 
    return 1;
}
