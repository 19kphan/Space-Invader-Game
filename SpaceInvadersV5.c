// Update positions for all alive sprites.
void Move(void){
  uint8_t num_life = 0;
    
	// Move Bullet
	
	if(start_shoot == 1){
		Bullet.x = PlayerShip.x + 8;
		Bullet.life = 1;
		Bullet.y = PlayerShip.y;
		start_shoot = 0;
		Sound_Shoot();
		//Delay100ms(100);
	}
	if(Bullet.life == 1){
		Bullet.y = Bullet.y - 2;
		
		if(Bullet.y <= 8){
			Bullet.life = 0;
		}
		
		for(int i = 0; i < ENEMY_AMOUNT; i++){
			if(Enemy[i].life == 1){
				if(doOverlap(Bullet.x, Bullet.y, Bullet.x+8, Bullet.y+1, Enemy[i].x+4, Enemy[i].y, Enemy[i].x+15, Enemy[i].y+5)){
					Bullet.life = 0;
					is_exploding[i] = 1;
				}
			}
		}
	}

	
	// detect hit

  // V2: Move enemies, check life or dead: dead if right side reaches right screen border or detect a hit   
	uint8_t i,j;
	if(movingDir == 0){
		// Moves the enemies to the right 
		for(i=0;i<ENEMY_AMOUNT;i++){
			if(Enemy[i].x < MAX_X-16){
				Enemy[i].x += 1;
				// Changes enemy into an explosion when hit 
				if(is_exploding[i] > 0){
					Enemy[i].image = SmallExplosion0;
				}
				else if((Enemy[i].x % 2) == 0){ // Changes enemy animations 
					Enemy[i].image = SmallEnemyPointA[i % 3];
				}else{
					Enemy[i].image = SmallEnemyPointB[i % 3];
				}
				
			}else{
				//Enemy[i].life = 0;
				if(Enemy[i].y > 30){
					Enemy[i].life = 0;
				}
				// Moves enemies down a row 
				if(Enemy[i].life == 1){
					for(j=0;j<ENEMY_AMOUNT;j++){
						Enemy[j].y += 7;
					}
					movingDir = 1;
				}
			}
			if(Enemy[i].life == 1){
				num_life += 1;
			}
		}
	}else{
		// Moves the enemies to the left 
		for(i=0;i<ENEMY_AMOUNT;i++){
			if(Enemy[i].x > 0){
				Enemy[i].x -= 1;
				// Changes enemy into an explosion when hit 
				if(is_exploding[i] > 0){
					Enemy[i].image = SmallExplosion0;
				}
				else if((Enemy[i].x % 2) == 0){ // Changes enemy animations 
					Enemy[i].image = SmallEnemyPointA[i % 3];
				}else{
					Enemy[i].image = SmallEnemyPointB[i % 3];
				}
				
			}else{
				// Moves enemies down a row 
				if(Enemy[i].life == 1){
					for(j=0;j<ENEMY_AMOUNT;j++){
						Enemy[j].y += 7;
					}
					movingDir = 0;
				}
			}
			// Adds enemy lives when the game starts
			if(Enemy[i].life == 1){
				num_life += 1;
			}
		}
		
	}
	
  
	// Read ADC and update player ship position: only x coordinate will be changed. 
	PlayerShip.x = (uint16_t) ((float)ADC0_InSeq3()*(POTRATE));
	
	// Ends game when enemy lives equals 0 
  if (num_life==0) {
    game_s = OVER;
  }
}

